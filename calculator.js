function sum(a, b) {
  if (!a || !b) {
    return;
  }
  return a + b;
}

function sub(a, b) {
  if (!a || !b) {
    return;
  }
  return a - b;
}

function multiply(a, b) {
  if (!a || !b) {
    return;
  }
  return a * b;
}

function division(a, b) {
  if (!a && !b) {
    return;
  }
  return a / b;
}